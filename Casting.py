
# type casting - sometimes we want to specify the type of variables
# OOP - Python is an object-oriented language so it defines the data types
# with classes !!!

# if the input is a float then Python will round the value
a = int(23.5)
print(a)
print(type(a))

# float() function
b = float('10')
print(b)
print(type(b))

# we can transform numbers into strings
c = str(100.5)
print(c)
print(type(c))
